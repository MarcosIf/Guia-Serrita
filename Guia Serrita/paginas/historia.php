      <p><h2 class="hist1"><br><br>HISTÓRIA DA CIDADE</h2></p><p>
        
 <br>
&nbsp; SERRITA fazia parte do antigo município de salgueiro do qual se destacou em 1928, constituindo-se e primeira chegada por vários familiares cearenses, vindo do vale do Cariri, os quais vinham se localizando nestas zonas e aqui formando núcleo de populações rurais.
 <br><br>
&nbsp;SERRITA originou-se do nome de SERRINHA, em virtude de uma pequena Serra ou Serrote do Cruzeiro, distante um (01) Km. da sede.<br>
 
&nbsp;Serrinha hoje é Serrita em virtude dos dispositivos contidos no Decreto Lei n° 952, de 31 de dezembro de 1934, que estabeleceu a divisão territorial judiciária administrativa do estado, vigorando no qüinqüênio 1944-49, no qual a Comarca de Serrita, com termo único, de igual número desmembrando da Comarca de Salgueiro.<br><br>
 
&nbsp;Não existia Tribos Indígenas no município, ignorando-se qualquer habitante que estivera nesta região antes do devastamento.
 
&nbsp;Com as famílias cearenses, negros vieram tomando parte do devastamento do município, sem elemento de valor no devastamento da terra.
 <br><br>
&nbsp;Os negros que constituíram elementos de real valor no devastamento da terra, provinham do Vale do Cariri no Estado do Ceará, isto se deu no Século XVIII, não é possível mencionar e data certa. Sabe-se que o primeiro habitante da sede foi MIGUEL TORQUATO DE BULHÕES, cearense que aqui construiu a primeira casa.<br><br>
 
&nbsp;Os primeiros edificadores construíram uma Capelinha Católica à Nossa Senhora da Conceição. onde veio o Padre de Salgueiro, vez por outra aparecia em desabrigo.<br><br>
 
&nbsp;A cidade de Serrita foi fundada em 1898, pelo Cel. Romão Pereira Fìlgueira Sampaio, cidadão que se instalara com as graças do Governo Estadual do seu tempo.<br><br>
 
&nbsp;&nbsp;Serrita foi elevada a município no dia 11 de setembro de 1928 pela Lei n° 1931, sendo extinta. A mesma foi restaurada em 01 de julho de 1934 por Decreto Lei n° 314 de 27 de junho de 1934.<br><br>
 
&nbsp;Na data de sua emancipação Política, era Governador do Estado Dr. Estácio de Albuquerque Coimbra.<br><br>
 
O território atual do município pertencia a diversos município como: Salgueiro, Parnamirim, Bodocó e Exu, dos quais foram desmembrados para constituir-se autônomo daí então seguir seu destino.<br><br>
 
 
A Legislação que criou os Distrito tem data do ano de 1928, ou seja, os seguintes distritos: SEDE, IPUEIRA e ORÍ.<br><br>
 
A freqüência foi criada em 1920. sob a Invocação de Padroeira Nossa Senhora da Conceição, sendo seu Primeiro Vigário o Padre Dr. Alfredo Guido Del Piazzo.<br><br>
 
